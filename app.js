async function submitReturn(event, outId, productName, originalQty, unitPrice, customerName, origDate) {
    event.preventDefault();
    try {
        showLoading();
        const retType = document.querySelector('input[name="retType"]:checked')?.value || 'all';
        const returnQty = parseFloat(document.getElementById('returnQtyInput')?.value || 0);
        const notes = document.getElementById('returnNotesInput')?.value.trim() || '';
        const empName = document.getElementById('returnEmpInput')?.value.trim() || '';

        if (returnQty <= 0 || returnQty > parseFloat(originalQty)) {
            hideLoading(); return;
        }

        // 1. Add returned quantity back to product stock automatically!
        if (getProductsAPI()) {
            await getProductsAPI().updateQuantity(productName, returnQty, parseFloat(unitPrice) || 0, '🔁 مرتجع من: ' + (customerName || 'عميل'));
        }

        // 2. Save the return record
        if (getInventoryReturnsAPI()) {
            await getInventoryReturnsAPI().add({
                out_id: outId,
                product_name: productName,
                quantity: returnQty,
                return_type: retType,
                customer_name: customerName,
                date: getTodayDate(),
                employee_name: empName,
                notes: notes,
                unit_price: parseFloat(unitPrice) || 0,
                original_quantity: parseFloat(originalQty) || 0
            });
        }

        closeCustomModal();
        showToast(t('returnSuccess'), 'success');

        if (AppState.currentPage === 'returns') await loadReturnsData();
        else if (AppState.currentPage === 'operations') await loadOperationsData();
        else await loadDashboardData();
    } catch (err) {
        // silent
    } finally {
        hideLoading();
    }
}


async function downloadOperationPDF(id, opType) {
    try {
        showLoading();
        let record;
        if (opType === 'in') {
            record = getInventoryInAPI() ? await getInventoryInAPI().getById(id) : null;
            if (record && getPDFGenerator()) await getPDFGenerator().generateInventoryInPDF(record, translations[AppState.currentLang]);
        } else {
            record = getInventoryOutAPI() ? await getInventoryOutAPI().getById(id) : null;
            if (record && getPDFGenerator()) await getPDFGenerator().generateInventoryOutPDF(record, translations[AppState.currentLang]);
        }
        if (!record) { hideLoading(); return; }
    } catch (err) {
        // Silent fail
    } finally {
        hideLoading();
    }
}


function previewSelectedImage(event) {
    const file = event.target.files?.[0];
    if (!file) return;
    const reader = new FileReader();
    reader.onload = function(e) {
        const img = new Image();
        img.onload = function() {
            const canvas = document.createElement('canvas');
            const MAX_SIZE = 500;
            let width = img.width;
            let height = img.height;
            if (width > height) {
                if (width > MAX_SIZE) {
                    height = Math.round(height * (MAX_SIZE / width));
                    width = MAX_SIZE;
                }
            } else {
                if (height > MAX_SIZE) {
                    width = Math.round(width * (MAX_SIZE / height));
                    height = MAX_SIZE;
                }
            }
            canvas.width = width;
            canvas.height = height;
            const ctx = canvas.getContext('2d');
            ctx.drawImage(img, 0, 0, width, height);
            const dataUrl = canvas.toDataURL('image/jpeg', 0.75);

            const hiddenInput = document.getElementById('editImageBase64');
            const previewDiv = document.getElementById('editImagePreview');
            const urlInput = document.getElementById('editImageUrl');
            if (hiddenInput) hiddenInput.value = dataUrl;
            if (urlInput) urlInput.value = '';
            if (previewDiv) {
                previewDiv.innerHTML = `<img src="${dataUrl}" style="width:100%;height:100%;object-fit:cover;" onerror="this.parentElement.innerHTML='📦'"/>`;
            }
        };
        img.src = e.target.result;
    };
    reader.readAsDataURL(file);
}

function previewUrlImage(event) {
    const url = event.target.value.trim();
    const hiddenInput = document.getElementById('editImageBase64');
    const previewDiv = document.getElementById('editImagePreview');
    const fileInput = document.getElementById('editImageFile');
    if (hiddenInput) hiddenInput.value = url;
    if (fileInput && url) fileInput.value = '';
    if (previewDiv) {
        if (url) {
            previewDiv.innerHTML = `<img src="${url}" style="width:100%;height:100%;object-fit:cover;" onerror="this.parentElement.innerHTML='📦'"/>`;
        } else {
            previewDiv.innerHTML = '📦';
        }
    }
}


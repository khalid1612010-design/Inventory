const AppState = {
    currentPage: 'dashboard',
    currentLang: 'en',
    products: [],
    inventoryIn: [],
    inventoryOut: []
};

const EMPLOYEES = [
    { ar: 'مي', en: 'empMai' },
    { ar: 'سهيلة', en: 'empSuhaila' },
    { ar: 'رحمة', en: 'empRahma' }
];

function getEmployeeOptions(selected = '') {
    const defaultOption = `<option value="">${t('selectEmployee')}</option>`;
    return defaultOption + EMPLOYEES.map(emp => {
        const label = t(emp.en) || emp.ar;
        const isSelected = emp.ar === selected || label === selected ? 'selected' : '';
        return `<option value="${emp.ar}" ${isSelected}>${label}</option>`;
    }).join('');
}

// ============================================================
// UTILITY FUNCTIONS
// ============================================================

function t(key) {
    return translations[AppState.currentLang][key] || key;
}

function formatCurrency(amount) {
    const num = parseFloat(amount) || 0;
    const symbol = t('currencySymbol') || 'ج.م';
    const formatted = new Intl.NumberFormat(AppState.currentLang === 'ar' ? 'ar-EG' : 'en-US', {
        minimumFractionDigits: 2,
        maximumFractionDigits: 2
    }).format(num);
    return `${formatted} ${symbol}`;
}

function formatNumber(num) {
    return new Intl.NumberFormat(AppState.currentLang === 'ar' ? 'ar-SA' : 'en-US').format(parseFloat(num) || 0);
}

function formatDate(dateStr) {
    if (!dateStr) return '-';
    return new Date(dateStr).toLocaleDateString(AppState.currentLang === 'ar' ? 'ar-SA' : 'en-US', {
        year: 'numeric', month: 'short', day: 'numeric'
    });
}

function escapeHtml(str) {
    if (!str) return '';
    const div = document.createElement('div');
    div.textContent = str;
    return div.innerHTML;
}

function showToast(message, type = 'success') {
    const container = document.getElementById('toastContainer');
    const toast = document.createElement('div');
    toast.className = `toast toast-${type}`;
    const icons = { success: '✓', error: '✕', warning: '⚠', info: 'ℹ' };
    toast.innerHTML = `<span>${icons[type] || ''}</span> ${message}`;
    container.appendChild(toast);
    setTimeout(() => {
        toast.style.opacity = '0';
        toast.style.transition = 'opacity 0.3s ease';
        setTimeout(() => toast.remove(), 300);
    }, 3500);
}

function showLoading() {
    document.getElementById('loadingOverlay').classList.add('active');
}

function hideLoading() {
    document.getElementById('loadingOverlay').classList.remove('active');
}

function getTodayDate() {
    return new Date().toISOString().split('T')[0];
}


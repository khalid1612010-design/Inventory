function validateOutQuantity() {
    const select = document.getElementById('outProductName');
    const selectedOption = select.selectedOptions[0];
    const qtyInput = document.getElementById('outQuantity');
    const hint = document.getElementById('outQuantityHint');
    if (selectedOption && selectedOption.dataset.quantity) {
        const available = parseFloat(selectedOption.dataset.quantity);
        const requested = parseFloat(qtyInput.value) || 0;
        if (requested > available) {
            hint.style.color = '#dc2626';
            hint.textContent = `⚠ ${t('insufficientStock')} ${formatNumber(available)}`;
        } else {
            hint.style.color = '';
            hint.textContent = `${t('stock')}: ${formatNumber(available)}`;
        }
    }
}

async function handleInventoryOutSubmit(event) {
    event.preventDefault();
    try {
        showLoading();
        const select = document.getElementById('outProductName');
        const selectedOption = select?.selectedOptions?.[0];
        const availableQty = selectedOption ? parseFloat(selectedOption.dataset.quantity || 0) : 0;
        const requestedQty = parseFloat(document.getElementById('outQuantity').value) || 0;

        if (requestedQty > availableQty || requestedQty <= 0) {
            hideLoading(); return;
        }

        const record = {
            product_name: document.getElementById('outProductName').value,
            quantity: requestedQty,
            total_selling_price: parseFloat(document.getElementById('outTotalPrice').value) || 0,
            customer_name: document.getElementById('outCustomerName').value.trim(),
            date: document.getElementById('outDate').value || getTodayDate(),
            employee_name: document.getElementById('outEmployeeName').value.trim(),
            notes: document.getElementById('outNotes').value.trim()
        };

        if (!record.product_name || !record.quantity) {
            hideLoading(); return;
        }

        if (getProductsAPI()) await getProductsAPI().updateQuantity(record.product_name, -record.quantity);
        if (getInventoryOutAPI()) await getInventoryOutAPI().add(record);
        showToast(t('recordAdded'), 'success');
        document.getElementById('inventoryOutForm').reset();
        document.getElementById('outDate').value = getTodayDate();
        const stockInfo = document.getElementById('outStockInfo');
        if (stockInfo) stockInfo.textContent = '';
        const qtyHint = document.getElementById('outQuantityHint');
        if (qtyHint) qtyHint.textContent = '';
        if (getProductsAPI()) AppState.products = await getProductsAPI().getAll();
        await loadProductsForSelect();
    } catch (err) {
        // Silent fail
    } finally {
        hideLoading();
    }
}


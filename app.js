function navigateTo(page) {
    AppState.currentPage = page;
    document.querySelectorAll('.sidebar-nav a').forEach(link => {
        link.classList.toggle('active', link.dataset.page === page);
    });
    renderPage(page);
    closeSidebar();
    const titles = {
        dashboard: t('dashboard'),
        'inventory-in': t('inventoryIn'),
        'inventory-out': t('inventoryOut'),
        products: t('products'),
        operations: t('operations')
    };
    document.getElementById('pageTitle').textContent = titles[page] || 'Kaizen';
}

function renderPage(page) {
    const content = document.getElementById('contentArea');
    switch (page) {
        case 'dashboard':
            content.innerHTML = renderDashboard();
            loadDashboardData();
            break;
        case 'inventory-in':
            content.innerHTML = renderInventoryInForm();
            initInventoryInForm();
            break;
        case 'inventory-out':
            content.innerHTML = renderInventoryOutForm();
            initInventoryOutForm();
            break;
        case 'products':
            content.innerHTML = renderProductsPage();
            loadProductsData();
            break;
        case 'operations':
            content.innerHTML = renderOperationsPage();
            loadOperationsData();
            break;
        default:
            content.innerHTML = renderDashboard();
            loadDashboardData();
    }
}

function toggleSidebar() {
    document.getElementById('sidebar').classList.toggle('open');
    document.getElementById('sidebarOverlay').classList.toggle('active');
}

function closeSidebar() {
    document.getElementById('sidebar').classList.remove('open');
    document.getElementById('sidebarOverlay').classList.remove('active');
}


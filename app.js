async function loadOperationsData() {
    try {
        showLoading();
        const [inRecords, outRecords] = await Promise.all([
            getInventoryInAPI() ? getInventoryInAPI().getAll() : [],
            getInventoryOutAPI() ? getInventoryOutAPI().getAll() : []
        ]);
        AppState.inventoryIn = inRecords;
        AppState.inventoryOut = outRecords;
        renderOperationsTable();
    } catch (err) {
        // Silent fail
    } finally {
        hideLoading();
    }
}

function getAllOperations() {
    return [
        ...AppState.inventoryIn.map(r => ({ ...r, opType: 'in', contactField: r.supplier_name, dateField: r.entry_date, priceField: r.unit_price })),
        ...AppState.inventoryOut.map(r => ({ ...r, opType: 'out', contactField: r.customer_name, dateField: r.date, priceField: r.total_selling_price }))
    ].sort((a, b) => new Date(b.created_at) - new Date(a.created_at));
}

function renderOperationsTable(ops = null) {
    const tbody = document.getElementById('operationsTableBody');
    if (!tbody) return;
    const operations = ops || getAllOperations();
    if (operations.length === 0) {
        tbody.innerHTML = `<tr><td colspan="8" class="empty-state"><div class="empty-icon">📋</div><p>${t('noOperations')}</p></td></tr>`;
        return;
    }
    tbody.innerHTML = operations.map(op => `
        <tr>
            <td><span class="badge ${op.opType === 'in' ? 'badge-in' : 'badge-out'}">${op.opType === 'in' ? '📥 ' + t('in') : '📤 ' + t('out')}</span></td>
            <td><strong>${escapeHtml(op.product_name)}</strong></td>
            <td>${formatNumber(op.quantity)}</td>
            <td>${op.opType === 'in' ? formatCurrency(op.priceField) + ' / ' + formatCurrency(op.total_price) : formatCurrency(op.total_selling_price)}</td>
            <td>${escapeHtml(op.contactField || '-')}</td>
            <td>${formatDate(op.dateField)}</td>
            <td>${escapeHtml(op.employee_name || '-')}</td>
            <td>
                <div style="display:flex;gap:0.4rem;align-items:center;">
                    <button class="btn btn-sm btn-outline" onclick="downloadOperationPDF('${op.id}', '${op.opType}')" title="${t('downloadPDF')}">
                        📄 PDF
                    </button>
                    ${op.opType === 'out' ? `<button class="btn btn-sm btn-outline" style="color:#d97706;border-color:#fcd34d;font-weight:bold;background:#fffbeb;" onclick="openReturnModal('${op.id}', '${escapeHtml(op.product_name).replace(/'/g, "\\'")}', '${op.quantity}', '${escapeHtml(op.contactField || '').replace(/'/g, "\\'")}', '${op.dateField || ''}', '${op.total_selling_price || 0}')" title="${t('processReturn')}">
                        🔁 ${t('processReturn')}
                    </button>` : ''}
                </div>
            </td>
        </tr>
    `).join('');
}

function filterOperationsTable() {
    const searchQuery = (document.getElementById('opsSearch')?.value || '').toLowerCase();
    const dateFilter = document.getElementById('opsDateFilter')?.value || '';
    const employeeFilter = (document.getElementById('opsEmployeeFilter')?.value || '').toLowerCase();
    const typeFilter = document.getElementById('opsTypeFilter')?.value || 'all';
    let ops = getAllOperations();
    if (searchQuery) ops = ops.filter(op => op.product_name.toLowerCase().includes(searchQuery) || (op.contactField && op.contactField.toLowerCase().includes(searchQuery)));
    if (dateFilter) ops = ops.filter(op => (op.dateField || '').split('T')[0] === dateFilter);
    if (employeeFilter) ops = ops.filter(op => op.employee_name && op.employee_name.toLowerCase().includes(employeeFilter));
    if (typeFilter !== 'all') ops = ops.filter(op => op.opType === typeFilter);
    renderOperationsTable(ops);
}

